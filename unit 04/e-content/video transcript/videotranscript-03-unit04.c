/*demonstration of switch case*/
#include<stdio.h>
int main()
{
    int i=4;
    switch(i)
    {
        case 1:
        printf("case1");
        break;
        case 2:
        printf("case2");
        break;
        case 3:
        printf("case3");
        break;
        case 4:
        printf("case4");
        break;
        default:
        printf("default");

    }
    return 0;
}